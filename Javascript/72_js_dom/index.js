// DOM = Document Object Model
// represent html structure tree
// โดยที่จะ ref ถึง tag ต่่างๆใน html เป็น hierarchy
// เราสามารถใช้ javascript access dom เพื่อทำการเปลี่ยนแปลง structure,style,content ได้
// document => คือการ access DOM ทั้งก้อน
// console.dir(document) => list all property ของ document
// document.title => access title ของ dom
// document.URL => access URL ของ dom
// document.location => access location url ของ dom
// document.body.style.backgroundColor = "skyblue" => เปลี่ยน background body เป็นสี skyblue
// document.getElementByID("key") => access element with id
// document.getElementByID("key").innerHTML => access content element
